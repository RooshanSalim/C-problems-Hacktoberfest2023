# Define two boolean variables
a = True
b = False

# Print the values of the variables
print(a)
print(b)

# Compare the two variables
if a == b:
  print("True")
else:
  print("False")

# Boolean operators
c = a and b
d = a or b
e = not a

# Print the results of the boolean operators
print(c)
print(d)
print(e)
